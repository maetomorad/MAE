					<h2>About The Making of Ancient Eurasia Project</h2>
					<p class="first">The Making of Ancient Eurasia (<a href="http://mae.uchicago.edu/">MAE</a>) project is an integrated interdisciplinary research consortium in which anthropologists from the <a target="_blank" href="http://anthropology.uchicago.edu/">University of Chicago</a>, <a target="_blank" href="http://anthropology.artsci.wustl.edu/">Washington University in Saint Louis</a>, <a target="_blank" href="http://anthropology.cornell.edu/">Cornell University</a>, <a target="_blank" href="http://www.isu.edu/anthro/">Idaho State University</a>, and materials scientists at Argonne National Laboratory (<a target="_blank" href="http://www.anl.gov">ANL</a>) and the <a target="_blank" href="http://www.synchrotron.org.au/">Australian Synchrotron</a> are collaborating in the simultaneous investigation of early ceramic and metal technologies in the Eurasian steppes, the Caucasus, and Central China. The MAE investigations are advancing the first combined, comparative anthropological and archaeometric study of ancient Eurasian ceramics and metalwork from the macro-, meso-, and micro- scales of analysis. This simultaneous analysis of ceramic and metal materials allows for a broader understanding of the production techniques, organization, and social and technical goals that drove sociocultural development on an interregional scale. Through intensive interactions with ANL and Asutralian Synchrotron scientists, our project offers opportunities for <a href="archaeometry.php">new archaeometric analytical techniques</a>, such as digital radiography (<a target="_blank" href="instrumentation.php#A1">DR</a>), X-ray computed tomography (<a target="_blank" href="instrumentation.php#A2">XCT</a>), and small- and wid-angle X-ray scattering (<a target="_blank" href="instrumentation.php#A5">SAXS/WAXS</a>), and their applications to be developed in direct conjunction with <a href="anthropology.php">anthropological questions</a>.</p>



